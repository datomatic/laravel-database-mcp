        LaravelSetList::LARAVEL_CODE_QUALITY,
        LaravelSetList::LARAVEL_COLLECTION,
        LaravelSetList::LARAVEL_IF_HELPERS,
        LaravelSetList::LARAVEL_TESTING,
    ])
    ->withSkip([
        EncapsedStringsToSprintfRector::class,
    ])
    ->withImportNames();
